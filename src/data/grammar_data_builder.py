def ensure_data_folder():
    if not os.path.exists(DATA_FOLDER):
        os.makedirs(DATA_FOLDER)


def get_csv_path(level):
    return os.path.join(
        DATA_FOLDER,
        f"jlpt_{level.lower()}_grammar.csv"
    )


def load_existing_rows(csv_path):
    if not os.path.exists(csv_path):
        return []

    rows = []

    with open(csv_path, "r", encoding="utf-8-sig", newline="") as file:
        reader = csv.DictReader(file)

        for row in reader:
            rows.append({
                "word": row.get("word", "").strip(),
                "hiragana": row.get("hiragana", "").strip(),
                "meaning": row.get("meaning", "").strip(),
                "romaji": row.get("romaji", "").strip(),
                "score": row.get("score", "100").strip(),
                "used": row.get("used", "false").strip(),
                "day": row.get("day", "").strip()
            })

    return rows


def make_row(grammar_item):
    word, hiragana, meaning, romaji = grammar_item

    return {
        "word": word,
        "hiragana": hiragana,
        "meaning": meaning,
        "romaji": romaji,
        "score": "100",
        "used": "false",
        "day": ""
    }


def merge_rows(existing_rows, new_rows):
    result = []
    used_keys = set()

    for row in existing_rows + new_rows:
        word = row.get("word", "").strip()
        romaji = row.get("romaji", "").strip()

        if not word or not romaji:
            continue

        key = romaji

        if key in used_keys:
            continue

        used_keys.add(key)
        result.append(row)

    return result


def save_rows(csv_path, rows):
    with open(csv_path, "w", encoding="utf-8-sig", newline="") as file:
        writer = csv.DictWriter(
            file,
            fieldnames=CSV_FIELDNAMES
        )

        writer.writeheader()

        for row in rows:
            writer.writerow(row)


def update_grammar_csv(level):
    csv_path = get_csv_path(level)

    existing_rows = load_existing_rows(csv_path)

    grammar_items = GRAMMAR_DATA.get(level, [])

    new_rows = []

    for item in grammar_items:
        new_rows.append(make_row(item))

    merged_rows = merge_rows(
        existing_rows,
        new_rows
    )

    save_rows(
        csv_path,
        merged_rows
    )

    print(level, "문법 CSV 갱신 완료:", len(existing_rows), "개 →", len(merged_rows), "개")


def update_all_grammar_csv():
    ensure_data_folder()

    for level in GRAMMAR_DATA:
        update_grammar_csv(level)

    print("전체 문법 CSV 갱신 완료")


if __name__ == "__main__":
    update_all_grammar_csv()